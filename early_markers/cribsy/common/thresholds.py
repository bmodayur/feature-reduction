"""Pre-computed Bayesian surprise thresholds with performance metrics.

This module contains empirically-derived threshold values for z-scores
(Bayesian surprise) along with their associated sensitivity, specificity,
and 95% confidence intervals from validation studies.

Data Structure:
    Each threshold dictionary contains:
    - threshold: Z-score cutoff value
    - sensitivity: True positive rate
    - sens_ci: 95% CI as string
    - sens_ci_lb, sens_ci_ub: CI bounds as floats
    - specificity: True negative rate
    - spec_ci: 95% CI as string  
    - spec_ci_lb, spec_ci_ub: CI bounds as floats

Threshold Arrays:
    - thresh_emcp_yt: Training on EMCP+Youtube (N=128), testing on CLIN (N=19)
    - thresh_emcp: Training on EMCP only (N=43), testing on CLIN (N=19)
    - thresh_yt: Training on Youtube only (N=85), testing on CLIN (N=19)

Datasets:
    - EMCP: Early Movement Assessment of Clinical Populations (at-risk infants)
    - Youtube: Normative development videos (typical development)
    - CLIN: Clinical validation cohort (mixed risk)

Usage:
    These thresholds can be used for:
    1. Comparing new model performance against validated baselines
    2. Selecting appropriate z-score cutoffs for desired Se/Sp balance
    3. Sample size estimation based on target precision

Note:
    Thresholds are ordered from high to low z-score.
    Higher thresholds = higher sensitivity, lower specificity
    Lower thresholds = lower sensitivity, higher specificity

Example:
    >>> from early_markers.cribsy.thresholds import thresh_emcp_yt
    >>> 
    >>> # Find threshold for ~90% sensitivity
    >>> for t in thresh_emcp_yt:
    ...     if 0.88 <= t['sensitivity'] <= 0.92:
    ...         print(f"Threshold: {t['threshold']}")
    ...         print(f"Sensitivity: {t['sensitivity']:.2f} {t['sens_ci']}")
    ...         print(f"Specificity: {t['specificity']:.2f} {t['spec_ci']}")
    ...         break
"""

